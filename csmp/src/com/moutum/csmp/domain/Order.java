public class Order
{
    
    /**
     * 订单ID
     */
    private int orderId;
    
    /**
     * 订单编号
     */
    private String orderNo;
    
    /**
     * 订单状态
     */
    private int orderState;
    
    /**
     * 下单日期
     */
    private Date orderDate;
    
    /**
     * 信息流
     */
    private String infoFlow;
    
    /**
     * 订单总额
     */
    private Double orderAmount;
    
    /**
     * 运费
     */
    private Double orderCarriage;
    
    /**
     * 订单周期:从放入第一件商品进购物车开始到生成订单的时间间隔
     */
    private int orderCycle;
    
    /**
     * 账户编号
     */
    private int userId;
    
    /**
     * 订单详情
     */
    private Set<OrderDetail> orderDetails;
    
    /**
     * 订单评价信息
     */
    private String evaluateInfo;
    
    /**
     * 订单申请取消原因
     */
    private String cancelReason;
    
    /**
     * 订单配送地址
     */
    private Address address;
    
    /**
     * 订单审核时间
     */
    private Date checkTime;
    
    /**
     * 订单支付时间
     */
    private Date payTime;
    
    /**
     * 收货时间
     */
    private Date takeGoodTime;
    
    /**
     * 评价时间
     */
    private Date evaluateTime;

    public int getOrderId()
    {
        return orderId;
    }

    public void setOrderId(int orderId)
    {
        this.orderId = orderId;
    }

    public String getOrderNo()
    {
        return orderNo;
    }

    public void setOrderNo(String orderNo)
    {
        this.orderNo = orderNo;
    }

    public int getOrderState()
    {
        return orderState;
    }

    public void setOrderState(int orderState)
    {
        this.orderState = orderState;
    }

    public Date getOrderDate()
    {
        return orderDate;
    }

    public void setOrderDate(Date orderDate)
    {
        this.orderDate = orderDate;
    }

    public String getInfoFlow()
    {
        return infoFlow;
    }

    public void setInfoFlow(String infoFlow)
    {
        this.infoFlow = infoFlow;
    }

    public Double getOrderAmount()
    {
        return orderAmount;
    }

    public void setOrderAmount(Double orderAmount)
    {
        this.orderAmount = orderAmount;
    }

    public Double getOrderCarriage()
    {
        return orderCarriage;
    }

    public void setOrderCarriage(Double orderCarriage)
    {
        this.orderCarriage = orderCarriage;
    }

    public int getOrderCycle()
    {
        return orderCycle;
    }

    public void setOrderCycle(int orderCycle)
    {
        this.orderCycle = orderCycle;
    }

    public int getUserId()
    {
        return userId;
    }

    public void setUserId(int userId)
    {
        this.userId = userId;
    }

    public Set<OrderDetail> getOrderDetails()
    {
        return orderDetails;
    }

    public void setOrderDetails(Set<OrderDetail> orderDetails)
    {
        this.orderDetails = orderDetails;
    }

    public String getEvaluateInfo()
    {
        return evaluateInfo;
    }

    public void setEvaluateInfo(String evaluateInfo)
    {
        this.evaluateInfo = evaluateInfo;
    }

    public String getCancelReason()
    {
        return cancelReason;
    }

    public void setCancelReason(String cancelReason)
    {
        this.cancelReason = cancelReason;
    }

    public Address getAddress()
    {
        return address;
    }

    public void setAddress(Address address)
    {
        this.address = address;
    }

    public Date getCheckTime()
    {
        return checkTime;
    }

    public void setCheckTime(Date checkTime)
    {
        this.checkTime = checkTime;
    }

    public Date getPayTime()
    {
        return payTime;
    }

    public void setPayTime(Date payTime)
    {
        this.payTime = payTime;
    }

    public Date getTakeGoodTime()
    {
        return takeGoodTime;
    }

    public void setTakeGoodTime(Date takeGoodTime)
    {
        this.takeGoodTime = takeGoodTime;
    }

    public Date getEvaluateTime()
    {
        return evaluateTime;
    }

    public void setEvaluateTime(Date evaluateTime)
    {
        this.evaluateTime = evaluateTime;
    }
}